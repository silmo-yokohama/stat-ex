/** スタッツ用語の定義 */
export type GlossaryEntry = {
  /** 英略称（例: "PPG"） */
  abbr: string;
  /** 正式英語名（例: "Points Per Game"） */
  fullName: string;
  /** 日本語の短い説明（例: "1試合あたりの平均得点"） */
  shortDesc: string;
  /** 初心者向けのやさしい説明 */
  description: string;
};

/** チャート補足情報 */
export type ChartHelpInfo = {
  /** チャートの概要（1行、セクション見出し下に常時表示） */
  summary: string;
  /** 詳細な説明（Popover内に表示） */
  details: string;
};

